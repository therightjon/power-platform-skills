#!/usr/bin/env node
'use strict';

// Writes two files into a working dir to make it editor-friendly:
//   package.json   — installable manifest with the deps from
//                    scripts/lib/supported-dependencies.js (Ask 2)
//   genpage.d.ts   — ambient global declarations only: `window.Xrm` and
//                    the bare `Xrm` identifier with the navigation/utility
//                    surface used by generative pages, plus the
//                    `__genpage_*` window cache-key index signature
//                    (Ask 3). `GeneratedComponentProps` is exported by
//                    RuntimeTypes.ts and is intentionally not re-declared
//                    here.
//
// Run by the /genpage orchestrator in Phase 0.5 (after working-dir creation).
// Idempotent by default — skips files that already exist. Pass --force to
// overwrite.
//
// Usage:
//   node generate-page-manifest.js <working-dir> <page-slug> [--features <list>] [--force]
//
// Arguments:
//   <working-dir>  Path to the working directory (relative or absolute)
//   <page-slug>    Used as the package "name" field (e.g., "account-card-gallery")
//
// Flags:
//   --features <list>  Comma-separated feature flags to include extra deps.
//                      Supported: charts, datepicker, timepicker
//   --force            Overwrite existing files instead of preserving them.
//
// Exit codes:
//   0 success
//   1 invalid arguments
//   2 I/O error

const fs = require('node:fs');
const path = require('node:path');
const {
  RUNTIME_DEPENDENCIES,
  DEV_DEPENDENCIES,
  buildDependencyMap,
  buildDevDependencyMap,
  FEATURE_RUNTIME_KEYS,
} = require('./lib/supported-dependencies.js');

const VALID_FEATURES = new Set(Object.keys(FEATURE_RUNTIME_KEYS));

function parseArgs(argv) {
  const args = { positional: [], features: [], force: false };
  for (let i = 0; i < argv.length; i++) {
    const a = argv[i];
    if (a === '--force') args.force = true;
    else if (a === '--features') {
      const list = argv[++i];
      if (!list) throw new Error('--features requires a comma-separated value');
      args.features = list.split(',').map((s) => s.trim()).filter(Boolean);
    } else if (a === '--help' || a === '-h') {
      printHelp();
      process.exit(0);
    } else if (a.startsWith('--')) {
      throw new Error(`Unknown flag: ${a}`);
    } else {
      args.positional.push(a);
    }
  }
  if (args.positional.length !== 2) {
    throw new Error('expected <working-dir> <page-slug> as positional arguments');
  }
  for (const f of args.features) {
    if (!VALID_FEATURES.has(f)) {
      throw new Error(`Unknown feature '${f}'. Valid: ${[...VALID_FEATURES].join(', ')}`);
    }
  }
  return args;
}

function printHelp() {
  console.log(`Usage: generate-page-manifest.js <working-dir> <page-slug> [options]

Options:
  --features <list>  Comma-separated feature flags (charts, datepicker, timepicker)
  --force            Overwrite existing package.json / genpage.d.ts
  --help             Show this message

Writes package.json and genpage.d.ts into <working-dir>.
`);
}

function buildPackageJson(slug, features) {
  return {
    name: slug,
    private: true,
    version: '0.0.1',
    description: `Local development manifest for genpage '${slug}' (auto-generated by /genpage Phase 0.5).`,
    dependencies: buildDependencyMap(features),
    devDependencies: buildDevDependencyMap(features),
    // No scripts — the runtime owns build and deploy.
    // `pac model genpage upload` reads <page>.tsx directly.
  };
}

// Static ambient declarations for the genux runtime. Same content for every
// working dir — no parameterization.
function buildAmbientDeclarations() {
  return `// Auto-generated by /genpage Phase 0.5 (do not edit by hand).
//
// Provides type definitions for runtime globals injected by the Power Apps
// model-driven app shell. After this file exists alongside the page's .tsx,
// the page can use \`window.Xrm.Navigation.navigateTo(...)\` without
// \`(window as any).\` casts.
//
// Regenerate by running:
//   node \${PLUGIN_ROOT}/scripts/generate-page-manifest.js <dir> <slug> --force

export {};

declare global {
    /** Subset of Xrm v9 surface used by generative pages. Extend as needed. */
    interface XrmShape {
        Navigation: {
            navigateTo: (
                pageInput: XrmPageInput,
                navigationOptions?: { target?: 1 | 2 },
            ) => Promise<XrmNavigationResult>;
            openForm: (
                formOptions: { entityName: string; entityId?: string },
                formParameters?: Record<string, unknown>,
            ) => Promise<unknown>;
            openUrl: (url: string, options?: { target?: 1 | 2 }) => void;
        };
        Utility: {
            getGlobalContext: () => XrmGlobalContext;
        };
    }

    type XrmPageInput =
        | { pageType: 'entityrecord'; entityName: string; entityId?: string }
        | { pageType: 'entitylist'; entityName: string }
        | {
            pageType: 'custom';
            pageId: string;
            entityName?: string;
            recordId?: string;
            data?: Record<string, unknown>;
        }
        | {
            /** Cross-page navigation between generative pages — see rules.md PAGEREF_ placeholder. */
            pageType: 'generative';
            pageId: string;
            entityName?: string;
            recordId?: string;
            data?: Record<string, unknown>;
        };

    interface XrmNavigationResult {
        savedEntityReference?: Array<{ entityType: string; id: string; name?: string }>;
    }

    interface XrmGlobalContext {
        userSettings: {
            userId: string;
            userName: string;
            languageId: number;
            dateFormattingInfo?: Record<string, unknown>;
        };
        organizationSettings: {
            organizationId: string;
            languageId: number;
            baseCurrency?: { id: string };
        };
        getClientUrl: () => string;
    }

    interface Window {
        /** Provided by the model-driven app shell. */
        Xrm: XrmShape;
    }

    /**
     * Bare \`Xrm\` identifier (used by generated pages as \`Xrm.Navigation...\`
     * without the \`window.\` prefix). Same shape as \`window.Xrm\`.
     */
    const Xrm: XrmShape;

    /**
     * Per-page cache keys MUST follow the pattern \`__genpage_<entity>_<v>\`
     * (see references/data-caching.md). The string-key index signature lets
     * TypeScript accept dynamic cache reads/writes without \`(window as any)\`.
     */
    interface Window {
        [cacheKey: \`__genpage_\${string}\`]: unknown;
    }
}
`;
}

function writeIfAllowed(filePath, content, force) {
  if (fs.existsSync(filePath) && !force) {
    return { wrote: false, reason: 'exists (use --force to overwrite)' };
  }
  try {
    fs.writeFileSync(filePath, content);
  } catch (err) {
    const e = new Error(`failed to write ${filePath}: ${err.message}`);
    e.code = 'IO_ERROR';
    throw e;
  }
  return { wrote: true };
}

function main() {
  let args;
  try {
    args = parseArgs(process.argv.slice(2));
  } catch (err) {
    console.error(`error: ${err.message}`);
    process.exit(1);
  }

  const workingDir = path.resolve(args.positional[0]);
  const slug = args.positional[1];

  try {
    if (!fs.existsSync(workingDir)) {
      console.error(`error: working directory does not exist: ${workingDir}`);
      process.exit(2);
    }
    if (!fs.statSync(workingDir).isDirectory()) {
      console.error(`error: not a directory: ${workingDir}`);
      process.exit(2);
    }
  } catch (err) {
    console.error(`error: failed to stat working directory: ${err.message}`);
    process.exit(2);
  }

  if (!/^[a-z][a-z0-9-]*$/.test(slug)) {
    console.error(`error: page-slug must be kebab-case (got: ${slug})`);
    process.exit(1);
  }

  let pkgResult;
  let dtsResult;
  try {
    const pkg = buildPackageJson(slug, args.features);
    const pkgPath = path.join(workingDir, 'package.json');
    pkgResult = writeIfAllowed(pkgPath, JSON.stringify(pkg, null, 2) + '\n', args.force);

    const dtsPath = path.join(workingDir, 'genpage.d.ts');
    dtsResult = writeIfAllowed(dtsPath, buildAmbientDeclarations(), args.force);
  } catch (err) {
    console.error(`error: ${err.message}`);
    process.exit(2);
  }

  const summary = {
    workingDir,
    slug,
    features: args.features,
    files: {
      'package.json': pkgResult,
      'genpage.d.ts': dtsResult,
    },
  };
  console.log(JSON.stringify(summary, null, 2));
}

if (require.main === module) main();

module.exports = {
  parseArgs,
  buildPackageJson,
  buildAmbientDeclarations,
  RUNTIME_DEPENDENCIES,
  DEV_DEPENDENCIES,
};
